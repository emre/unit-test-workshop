
import math

class Calc:

    def add(self, a, b):
        return a + b

    def substract(self, a, b):
        return a - b

    def multiply(self, a, b):
        return a * b

    def divide(self, a, b):
        return a / b

    def factorial(self, number):
        return math.factorial(number)

    def sqrt(self, number):
        return math.sqrt(number)

